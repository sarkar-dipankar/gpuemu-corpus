"""gpuemu-corpus — validated 26-op kernel corpus.

The corpus is the same one P1–P4 was measured on: 15 correct controls + 9
LLM-style buggy variants of real Triton kernels (softmax, gelu, silu, rmsnorm,
l2norm, leaky_relu, matmul, attention, flash-attention) + 2 sanity controls.

Each op ships:
  - meta.json: name, source ("human:triton" or "llm:..."), benchmark_verdict,
    input_names, reference path, kernel path, dtypes, tolerances, op_schema.
  - ref_fp64.py: a high-precision fp64 reference invoked by the gpuemu daemon.
  - kernel.py: the kernel under test (for the LLM-buggy variants).

Public surface:
  - ``corpus_dir() -> Path``: absolute path to the bundled corpus tree.
  - ``list_ops() -> list[str]``: every registered op name.
  - ``meta(name: str) -> dict``: the meta.json for one op.
  - ``write_gpuemu_toml(out: Path) -> Path``: emit a gpuemu.toml that points the
    daemon at the bundled corpus with absolute reference paths.
  - ``register_all(client, *, toml_path=None) -> Path``: convenience that writes
    the gpuemu.toml next to the daemon's config and returns its path. The
    daemon must be restarted to pick up the new config.

The corpus data lives under ``gpuemu_corpus/data/``. In a source checkout, this
directory is a symlink to ``../gpuemu-paper/corpus/`` so the canonical
research source stays the single source of truth. In a built wheel, the data
is vendored at release time via ``scripts/vendor_corpus.py``.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Optional

__version__ = "0.1.0"

_HERE = Path(__file__).resolve().parent
_DATA = _HERE / "data"


def corpus_dir() -> Path:
    """Return the absolute path to the bundled corpus directory."""
    if not _DATA.exists():
        raise RuntimeError(
            "gpuemu_corpus data directory missing — the wheel was built without "
            "vendoring. Run `scripts/vendor_corpus.py` against a gpuemu-paper "
            "checkout to populate it, or install from a release wheel."
        )
    return _DATA


def list_ops() -> list[str]:
    """Return every op name registered in the bundled corpus."""
    return sorted(
        json.loads(m.read_text())["name"] for m in corpus_dir().glob("*/meta.json")
    )


def meta(name: str) -> dict:
    """Return the meta.json for a single op."""
    for m in corpus_dir().glob("*/meta.json"):
        loaded = json.loads(m.read_text())
        if loaded["name"] == name:
            return loaded
    raise KeyError(name)


def _inline_tol(tol: dict) -> str:
    body = ", ".join(f"{k} = {v}" for k, v in tol.items())
    return "{ " + body + " }"


def write_gpuemu_toml(out: Path) -> Path:
    """Emit a gpuemu.toml at ``out`` registering every bundled op.

    Reference paths are absolute, anchored at ``corpus_dir()``, so the daemon
    can be started from any working directory.
    """
    out = Path(out)
    corpus = corpus_dir()
    lines = [
        "# AUTO-GENERATED by gpuemu_corpus.write_gpuemu_toml. Do not edit by hand.",
        "",
        "[project]",
        'name = "gpuemu-corpus"',
        'framework = "pytorch"',
        "",
        "[validation]",
        'dtypes = ["float32", "float16"]',
        "check_nan = true",
        "check_inf = true",
        "tolerances = { float32 = 1e-5, float16 = 1e-2 }",
    ]
    for m in sorted(corpus.glob("*/meta.json")):
        m_obj = json.loads(m.read_text())
        names = ", ".join(f'"{n}"' for n in m_obj["input_names"])
        ref_abs = (corpus / m_obj["reference"]).resolve()
        lines += [
            "",
            "[[ops]]",
            f'name = "{m_obj["name"]}"',
            f'reference = "{ref_abs}"',
            f"input_names = [{names}]",
            'execution_mode = "client_side"',
        ]
        if m_obj.get("tolerances"):
            lines.append(f"tolerances = {_inline_tol(m_obj['tolerances'])}")
    out.write_text("\n".join(lines) + "\n")
    return out


def register_all(client, *, toml_path: Optional[Path] = None) -> Path:
    """Convenience: write the bundled corpus's gpuemu.toml and return its path.

    ``client`` is currently unused — the daemon reads its op registry from
    ``gpuemu.toml`` at startup, so registration is a config-file write rather
    than a runtime RPC. Reserved for future versions that may expose a
    daemon-side add-op API.
    """
    del client  # reserved for future daemon-side registration API
    if toml_path is None:
        toml_path = Path.cwd() / "gpuemu-corpus.toml"
    return write_gpuemu_toml(toml_path)


__all__ = [
    "__version__",
    "corpus_dir",
    "list_ops",
    "meta",
    "register_all",
    "write_gpuemu_toml",
]
